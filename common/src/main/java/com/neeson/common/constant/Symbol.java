    String WAVE_HYPHEN = "~";
    /** 波纹连字符带空格 */
    String WAVE_HYPHEN_WITH_SPACE = " ~ ";
    /** 间隔号带空格 */
    String INTERPUNCT = "·";
    /** 中文冒号 */
    String CHINESE_COLON = "：";
